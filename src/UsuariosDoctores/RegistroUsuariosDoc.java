    private void limpiarFormulario (){
        comboBoxDoctores();
        txt_Contraseña.setText("");
        txt_IdDoc.setText("");
        txt_User.setText("");
    }
    
    @SuppressWarnings("UseSpecificCatch")
    public void arrojarIdDoctor (){
        if (!llave) return;
        if (jComboBox1.getSelectedIndex()==0) {
            txt_IdDoc.setText("");
            return;
        }
        try{
            DatosDoctores dt=new DatosDoctores();
            ResultSet rs=dt.regresarIDDoctor(String.valueOf(jComboBox1.getSelectedItem()));
            rs.next();
            txt_IdDoc.setText(rs.getString(1));
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    public boolean insertarUserDoc (){
        try{
            String datos[]={txt_IdDoc.getText(),txt_User.getText(),txt_Contraseña.getText()};
            DatosUsuariosDoc dt=new DatosUsuariosDoc();
            return dt.insertarUsuarioDoc(datos);
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        return false;
    }
    
    @SuppressWarnings("UseSpecificCatch")
    public void comboBoxDoctores (){
        llave=false;
        jComboBox1.removeAllItems();
        jComboBox1.addItem("-Seleccione-");
        try{
            DatosDoctores dt=new DatosDoctores();
            ResultSet rs=dt.buscarComboDoctor();
            
            while (rs.next()){
                jComboBox1.addItem(rs.getString(1));
            }
            dt.cerrarConeccion();
            llave=true;
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
